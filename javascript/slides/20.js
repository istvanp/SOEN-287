// @title Operands: NOT, Less than, Greater than
// @desc <, >, <=, >=, !

document.writeln( ! true);

var expr = null; // try different values here
document.writeln( !! expr); // Coerce into a boolean

document.writeln(10  > 10);
document.writeln(10 >= 10);
document.writeln(3   < 10);
document.writeln(10 <= 10);

